public class ifelse {
    public static void main(String[] args) {
        /*
         * syntax for if else
         * if(condition){
         * condition to be executed
         * }else{
         * condition to be executed
         * }
         */
        boolean rain = true;
        if (rain) {
            System.out.println("Take an umbrella");
        } else {
            System.out.println("Enjoy your day");
        }
    }
}
